  "print(output.shape)  # (10, 1)",
  "```",
  "",
  "### الانتشار الخلفي (Backpropagation)",
  "",
  "كيف تتعلم الشبكة؟ عبر **الانتشار الخلفي** و**الانحدار التدريجي (Gradient Descent)**:",
  "",
  "1. **حساب الخطأ**: الفرق بين التوقع والحقيقة",
  "2. **حساب التدرج**: مشتقة الخطأ بالنسبة لكل وزن (بقاعدة السلسلة)",
  "3. **تحديث الأوزان**: `w = w - learning_rate * gradient`",
  "",
  "```python",
  "# انحدار تدريجي مبسط",
  "def train_step(X, y, W, b, lr=0.01):",
  "    # forward",
  "    y_pred = sigmoid(X @ W + b)",
  "",
  "    # loss (binary cross-entropy)",
  "    loss = -np.mean(",
  "        y * np.log(y_pred + 1e-9) +",
  "        (1 - y) * np.log(1 - y_pred + 1e-9)",
  "    )",
  "",
  "    # gradients",
  "    error = y_pred - y",
  "    grad_W = X.T @ error / len(y)",
  "    grad_b = np.mean(error)",
  "",
  "    # update",
  "    W -= lr * grad_W",
  "    b -= lr * grad_b",
  "    return W, b, loss",
  "```",
  "",
  "### دالة الخسارة (Loss Function)",
  "",
  "- **MSE**: للانحدار",
  "- **Binary Cross-Entropy**: للتصنيف الثنائي",
  "- **Categorical Cross-Entropy**: للتصنيف المتعدد",
