  "  (x3) ────── (h3)",
  "             (h4)",
  "```",
  "",
  "- **طبقة الإدخال (Input Layer)**: تستقبل الميزات",
  "- **الطبقات المخفية (Hidden Layers)**: تتعلم التمثيلات",
  "- **طبقة الإخراج (Output Layer)**: تعطي النتيجة",
  "",
  "كلما زادت الطبقات المخفية، زادت قدرة الشبكة على تعلم علاقات معقدة.",
  "",
  "### دوال التفعيل (Activation Functions)",
  "",
  "تُدخل اللاخطية — بدونها الشبكة مجرد انحدار خطي مهما زادت طبقاتها.",
  "",
  "```python",
  "import numpy as np",
  "",
  "# Sigmoid: (0, 1) — للإخراج الثنائي",
  "def sigmoid(z):",
  "    return 1 / (1 + np.exp(-z))",
  "",
  "# ReLU: (0, ∞) — الأكثر شيوعاً في الطبقات المخفية",
  "def relu(z):",
  "    return np.maximum(0, z)",
  "",
  "# Tanh: (-1, 1) — متمركز حول الصفر",
  "def tanh(z):",
  "    return np.tanh(z)",
  "",
  "# Softmax: توزيع احتمالي — للتصنيف المتعدد",
  "def softmax(z):",
  "    exp_z = np.exp(z - np.max(z))  # ثبات عددي",
  "    return exp_z / exp_z.sum(axis=0)",
  "```",
  "",
  "### الانتشار الأمامي (Forward Pass)",
  "",
  "```python",
  "import numpy as np",
  "",
  "def forward(X, W1, b1, W2, b2):",
  "    # الطبقة المخفية",
  "    z1 = X @ W1 + b1        # مجموع موزون",
  "    a1 = np.maximum(0, z1)  # ReLU",
  "",
  "    # طبقة الإخراج",
  "    z2 = a1 @ W2 + b2",
  "    probs = 1 / (1 + np.exp(-z2))  # sigmoid",
  "    return probs",
  "",
  "# مثال: 4 ميزات دخل، 5 خلايا مخفية، إخراج واحد",
